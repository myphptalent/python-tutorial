# Exammple for the escape sequence

# output : Line A \n Line B
print("Line A \\n Line B")

# output : Line A \t Line B
print("Line A \\t Line B")

#output : This is a single backslash \ 
print("This is a single backslash \\")

#output : This is 4 backslash \\\\
print("This is 4 backslash \\\\\\\\")

#output : \" \'

print("\\\" \\\'")


#output : This is mountains /\/\/\/\/\

print("/\\/\\/\\/\\/\\")

#output : \" \n \t \'

print("\\\"  \\n \\t \\' ")
